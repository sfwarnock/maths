 Numbers
 natural numbers =  {1, 2, 3, 4, 5, ...}
 whole numbers =    {0, 1, 2, 3, 4, 5, ...}
 integer numbers =  {..., -3, -2, -1, 0, 1, 2, 3, ...}
 rational numbers = { m / n | m & n == interger & n != 0}
                    1. terminating decimal: 15/8 = 1.875
                    2. reapting decimal: 4/11 = 0.36363636...
 irrational numbers = {h|h is not a ratiional number}

 Order of Operations
 Parentheses, Exponents, Multiplication and Divison, Addition and Subtraction (PEMDAS)
 
 Properties of Real Numbers
 Commutative Properties
    Commutative property of addition: numbers may be added in any order without affecting the sum.
            a + b = b + a
    Commutative property of multiplication: numbers may be multiplied in any order affeting the product.
            a * b = b * a
 
 Associative Properties
    Associative property of multiplication: it does not matter how we group numbers when multiplying.
            a(bc) = (ab)c
    Associative property of addition: numbers may be grouped differently without affecting the sum.
            a + (b + c) = (a + b) + c
 
Distributive Property
    Distributive Property: the product of a factor times a sume is the sume of the factor times each term in the sum.
            a * (b+c) = a * b + a * c
 
