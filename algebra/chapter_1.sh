 Properties of Real Numbers
 Commutative Properties
    Commutative property of addition: numbers may be added in any order without affecting the sum.
            a + b = b + a
    Commutative property of multiplication: numbers may be multiplied in any order affeting the product.
            a * b = b * a
 
 Associative Properties
    Associative property of multiplication: it does not matter how we group numbers when multiplying.
            a(bc) = (ab)c
    Associative property of addition: numbers may be grouped differently without affecting the sum.
            a + (b + c) = (a + b) + c
 
Distributive Property
    Distributive Property: the product of a factor times a sume is the sume of the factor times each term in the sum.
            a * (b+c) = a * b + a * c
 
Identity Properties
    Identity property of addition: there is a unique number, called the additive identity (0) that, when added to number, results in the original number.
            a + 0 = a
    Identity propoerty of multiplication: there is a unique number, called the multiplicative indentify (1) that, when multiplied by a number, results in the original number.
            a * 1 = a

Inverse Properties
    Inverse property of addition: for every real number a, there is a unique number, called the additive inverse (or opposite), denoted -a, that when added to the original number, results in the additive identity, 0.
            a + (-a) = 0
    Inverse property of multiplication: holds for all real numbers execpt 0 because the reciprocal of 0 is not defined. For every real number a, there is a unique number, called the multiplicative inverse (or reciprocal), denoted 1/a, that when multiplied by the original number, results in the multiplicative identity, 1.
            a * 1/a = 1


Exponents and Scientific Notation
    Product rule of exponents: for any real numbers a and natural numbers m and n, the product rule of exponents states that:
            a^m * a^n = a^(m + n)
            
    Quotient rule of exponents: for any real numbers a and natural numbers m and n, such that m > n, the quotient rule of exponents states that:
            a^m / a^n = a^(m - n)
    
    Power rule of exponents: for any real number a and positive integers m and n, the power rule of exponents states that:
            (a^m)^n = a^(m * n)
    
    Zero exponent rule of exponents: for any nonzero real number a, that zero exponent rule of exponents states that:
            a^0 = 1
